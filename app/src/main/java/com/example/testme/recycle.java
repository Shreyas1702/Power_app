package com.example.testme;

public class recycle {
    String app_name ;
    int app_numb ;

    public recycle(String app_name, int app_numb) {
        this.app_name = app_name;
        this.app_numb = app_numb;
    }

    public String getApp_name() {
        return app_name;
    }

    public int getApp_numb() {
        return app_numb;
    }
}
